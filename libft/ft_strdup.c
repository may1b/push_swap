#include <stddef.h>
#include <stdlib.h>

char	*ft_strdup(const char *src)
{
	size_t		i;
	char		*dup;

	i = 0;
	while (src[i])
		i++;
	dup = malloc(i + 1);
	if (!dup)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
